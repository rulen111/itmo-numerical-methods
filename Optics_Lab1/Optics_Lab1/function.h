#define FUNCTION_H

#include <cmath>
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
class Function
{
public:
	//конструктор
	Function();
	//деструктор
	~Function();

	// проба функции
	double ProbaFunction(double x, double y);
	// пробы производной по х
	double ProbaDiffRoX(double x, double y);
	// пробы производной по y
	double ProbaDiffRoY(double x, double y);
};
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//Реализация методов

//Конструктор
Function::Function() {}

//Деструктор
Function::~Function() {}

//Проба функции
double Function::ProbaFunction(double x, double y)
{
	//return (2 * pow(x, 2) + 2 * pow(y, 2) - 1);
	return (pow(y, 2) + y - pow(x, 2));
}

// пробы производной по х
double Function::ProbaDiffRoX(double x, double y)
{
	//return (4 * x);
	return (-2 * x);
}

// пробы производной по y
double Function::ProbaDiffRoY(double x, double y)
{
	//return (4 * y);
	return (2 * y + 1);
}
